    private void ButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonModificarActionPerformed
        if (Taula.getSelectedRow() == -1) {
            ButtonEliminar.setEnabled(false);
            ButtonModificar.setEnabled(false);
        } else {
            cnt.ObrirFormulari(parent, getIdVaixellSeleccionat());
            setInfoCategories();
        }
    }//GEN-LAST:event_ButtonModificarActionPerformed

    private int getIdVaixellSeleccionat() {
        return Integer.parseInt(Taula.getValueAt(Taula.getSelectedRow(), 0).toString());
    }
    private void TaulaFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_TaulaFocusGained
        if (Taula.getSelectedRow() != -1) {
            ButtonEliminar.setEnabled(true);
            ButtonModificar.setEnabled(true);
        }
    }//GEN-LAST:event_TaulaFocusGained

    private void TaulaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_TaulaFocusLost
        if (Taula.getSelectedRow() == -1) {
            ButtonEliminar.setEnabled(false);
            ButtonModificar.setEnabled(false);
        }
    }//GEN-LAST:event_TaulaFocusLost

    private void ButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEliminarActionPerformed
        if (Taula.getSelectedRow() != -1) {
            cnt.eliminarVaixell(getIdVaixellSeleccionat());
            setInfoCategories();
        } // Double IF per a comprovar tant si s'elimina una categoria com si no
        if (Taula.getSelectedRow() == -1) {
            ButtonEliminar.setEnabled(false);
            ButtonModificar.setEnabled(false);
        }
    }//GEN-LAST:event_ButtonEliminarActionPerformed

