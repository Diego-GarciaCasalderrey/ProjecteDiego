    /**
     * Mostra totes les categories dins d'un ComboBox, incloent-hi una que
     * representa qualsevol categoria.
     */
    public void MostrarCategories() {
        ComboBoxCategoria.removeAllItems();
        ComboBoxCategoria.addItem("-- Tot --");
        cnt.getCategories("nom").forEach((c) -> {
            ComboBoxCategoria.addItem(c.getNom());
        });
    }

    /**
     * Mostra totes les columnes segons les quals es podria ordenar,
     * dins d'un ComboBox.
     * @param tm retorna el TableModel de la Taula.
     */
    public void MostrarOrdres(TableModel tm) {
        String[] array = cnt.getTaulaOrdre(tm);
        for (String x : array) {
            ComboBoxOrdre.addItem(x);
        }
    }

    /**
     * Inicialitza el TableModel quan encara no s'han seleccionat valors.
     * @return retorna el TableModel de la Taula.
     */
    public TableModel setDefault() {
        try {
            tm = cnt.getVaixellsTable();
            Taula.setModel(tm);
            return tm;
        } catch (SQLException ex) {
            System.out.println(ex.toString());
            return null;
        }
    }

    /**
     * Actualitza el TableModel segons el que l'usuari hagi seleccionat tant al
     * ComboBox de filtre per categoria com el de l'ordre per columna.
     */
    public void setInfoCategories() {
        try {
            tm = cnt.getVaixellsTable(ComboBoxOrdre.getSelectedItem().toString(),
                    cnt.getIdCategoria(cnt.getCatByOrderIndex(ComboBoxCategoria.getSelectedIndex() - 1)));
            Taula.setModel(tm);
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }

